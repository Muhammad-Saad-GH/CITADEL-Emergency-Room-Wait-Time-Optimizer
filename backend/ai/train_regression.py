import pickle
import numpy as np
from sklearn.linear_model import LinearRegression

# --------------------------------
# Training dataset (severity, wait)
# --------------------------------
data = [
    # Severity 1
    (1,8),(1,5),(1,6),(1,15),(1,10),(1,11),(1,9),(1,11),(1,7),(1,13),
    (1,4),(1,8),(1,10),(1,10),(1,8),(1,12),(1,11),(1,13),(1,13),(1,9),

    # Severity 2
    (2,18),(2,22),(2,20),(2,25),(2,19),(2,23),(2,21),(2,24),(2,17),(2,26),
    (2,25),(2,28),(2,22),(2,30),(2,24),(2,27),(2,23),(2,29),(2,21),(2,31),

    # Severity 3
    (3,35),(3,67),(3,48),(3,30),(3,40),(3,34),(3,46),(3,64),(3,59),(3,83),
    (3,40),(3,45),(3,83),(3,75),(3,72),(3,47),(3,59),(3,58),(3,71),(3,90),

    # Severity 4
    (4,178),(4,221),(4,94),(4,156),(4,203),(4,137),(4,189),(4,165),(4,211),(4,149),
    (4,198),(4,172),(4,184),(4,230),(4,160),(4,207),(4,143),(4,219),(4,168),(4,240),


    # Severity 5
    (5,276),(5,249),(5,263),(5,241),(5,287),(5,255),(5,269),(5,248),(5,295),(5,252),
    (5,284),(5,291),(5,266),(5,300),(5,289),(5,294),(5,271),(5,297),(5,283),(5,299),

]

# --------------------------------
# Prepare data for regression
# --------------------------------
X = np.array([d[0] for d in data]).reshape(-1, 1)  # severity
y = np.array([d[1] for d in data])                 # wait time

# --------------------------------
# Train model
# --------------------------------
model = LinearRegression()
model.fit(X, y)

# --------------------------------
# Save model
# --------------------------------
with open("severity_regression.pkl", "wb") as f:
    pickle.dump(model, f)

# --------------------------------
# Output learned equation
# --------------------------------
a = model.coef_[0]
b = model.intercept_

print("Regression trained successfully.")
print(f"Learned model: wait_time = {a:.2f} * severity + {b:.2f}")
